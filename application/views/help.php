<h2>Help</h2>

<h3>Reporting Issues</h3>
<p>Please send any issues and/or feedback to <a href="mailto:pete.j.scott?subject=4divisions+2.0+issue">pete.j.scott@gmail.com</a></p>

<h3>Legend for Bugs/Features</h3>
<p>Bugs take precedence over Roadmap Features. Within those categories, there are three levels of importance:</p>
<ul>
	<li class="major">Major Defects/Features. These will receive attention first.</li>
	<li class="medium">Medium Defects/Features. These will be addressed after Major issues (or before, if they're quick and painless)</li>
	<li class="minor">Minor Defects/Features. These will be addressed after Major and Medium issues (or before, if they're quick and painless)</li>
</ul>

